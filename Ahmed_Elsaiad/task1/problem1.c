#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

unsigned int arr[1000];

int main() {

    unsigned int arr_size;
    unsigned int count , res=0;
    //printf("Enter the array size ->");
    scanf("%i",&arr_size);
    //printf("Enter the array elements\n");
    for (count=0 ; count < arr_size ; count++)
    {
        scanf("%i",&arr[count]);
        fflush(stdin);
        res+=arr[count];
    }
    printf("%i",res);
    return 0;
}
